//numero entero - integers

var edad = 25;
console.log(edad);
console.log(typeof edad);

var poblacion = 32600400;
console.log(poblacion);
console.log(typeof poblacion);

// numeros flotantes - floats

var pi = 3.1416;
console.log(pi);
console.log(typeof pi);

var pulgada = 0.0254;
console.log(pulgada);


//NaN - Not a Number 

var nan = 'hola' * 2;
console.log(nan);
console.log(typeof nan);

//infinity

var infinito = -100 / 0;
console.log(infinito);
console.log(typeof infinito);